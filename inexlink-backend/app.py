@app.route("/api/feature_importance")
def feature_importance():
    """
    Live feature importance extracted directly from the trained model.
    Tree-based models use feature_importances_, Ridge uses abs(coef_).

    Optional query params:
        model_name: ridge | random_forest | xgboost  (default: xgboost)
        top_n: number of top features to return (default: 10)
    """
    model_name = request.args.get("model_name", DEFAULT_MODEL).strip().lower()
    top_n = int(request.args.get("top_n", 10))

    try:
        pipeline = load_model(model_name)
    except (ValueError, FileNotFoundError) as e:
        return error_response(str(e), 500)

    try:
        preprocessor = pipeline.named_steps["pre"]
        regressor    = pipeline.named_steps["reg"]
        feature_names = preprocessor.get_feature_names_out()

        if hasattr(regressor, "feature_importances_"):
            importances = regressor.feature_importances_
        elif hasattr(regressor, "coef_"):
            importances = np.abs(regressor.coef_)
        else:
            return error_response(f"Model '{model_name}' does not expose feature importances.", 400)

        clean_names = [clean_feature_name(str(n)) for n in feature_names]

        pairs = sorted(
            zip(clean_names, importances.tolist()),
            key=lambda x: x[1],
            reverse=True
        )[:top_n]

        result = [{"feature": name, "importance": round(imp, 4)} for name, imp in pairs]

        return jsonify({
            "success": True,
            "model_used": model_name,
            "model_display_name": MODEL_REGISTRY[model_name]["display_name"],
            "feature_importance": result,
        })

    except Exception as e:
        return error_response(f"Failed to extract feature importance: {str(e)}", 500)


@app.route("/api/predictive_insights")
def predictive_insights():
    """
    Run live predictions on sample listings using the active model.
    Replaces the hardcoded predictive insights table in the dashboard.

    Optional query param:
        model_name: ridge | random_forest | xgboost  (default: xgboost)
    """
    model_name = request.args.get("model_name", DEFAULT_MODEL).strip().lower()

    try:
        pipeline = load_model(model_name)
    except (ValueError, FileNotFoundError) as e:
        return error_response(str(e), 500)

    try:
        results = []
        for listing in SAMPLE_LISTINGS:
            df = pd.DataFrame([{k: listing[k] for k in PREDICTION_FEATURES}])
            predicted_days = max(0.1, round(float(pipeline.predict(df)[0]), 1))
            results.append({
                "id":            listing["id"],
                "equipment":     listing["equipment"],
                "listingPrice":  listing["listing_price"],
                "estimatedDays": predicted_days,
                "confidence":    [round(predicted_days * 0.8, 1), round(predicted_days * 1.2, 1)],
            })

        return jsonify({
            "success": True,
            "model_used": model_name,
            "model_display_name": MODEL_REGISTRY[model_name]["display_name"],
            "insights": results,
        })

    except Exception as e:
        return error_response(f"Failed to generate insights: {str(e)}", 500)


@app.route("/api/predict", methods=["POST"])
def predict():
    """
    Predict days to sale for a mining equipment listing.
    Optional query param: model_name (default: xgboost)
    """
    model_name = request.args.get("model_name", DEFAULT_MODEL).strip().lower()

    try:
        pipeline = load_model(model_name)
    except ValueError as e:
        return error_response(str(e), 400)
    except FileNotFoundError as e:
        return error_response(str(e), 500)

    data = request.get_json(force=True, silent=True)
    if not data:
        return error_response("Request body must be valid JSON.", 400)

    missing = [f for f in PREDICTION_FEATURES if f not in data]
    if missing:
        return error_response(f"Missing required fields: {missing}", 400)

    try:
        df = pd.DataFrame([data])
        predicted_days = round(float(pipeline.predict(df)[0]), 1)
        return jsonify({
            "success": True,
            "model_used": model_name,
            "model_display_name": MODEL_REGISTRY[model_name]["display_name"],
            "estimated_days_to_sale": predicted_days,
            "estimated_weeks": round(predicted_days / 7, 1),
            "confidence_interval": [
                round(predicted_days * 0.8, 1),
                round(predicted_days * 1.2, 1),
            ],
        })
    except Exception as e:
        return error_response(f"Prediction failed: {str(e)}", 500)


