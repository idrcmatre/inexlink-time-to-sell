def clean_feature_name(raw: str) -> str:
    """Strip sklearn prefixes and apply human-readable mapping."""
    stripped = raw.replace("num__", "").replace("cat__", "").replace("bin__", "")
    if stripped in FEATURE_NAME_MAP:
        return FEATURE_NAME_MAP[stripped]
    # Fallback: replace underscores and title-case
    return stripped.replace("_", " ").title()


