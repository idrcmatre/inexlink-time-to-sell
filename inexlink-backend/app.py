def load_model(model_name: str):
    """Load model from disk into cache. Returns pipeline or raises."""
    if model_name not in MODEL_REGISTRY:
        raise ValueError(f"Unknown model '{model_name}'. Available: {list(MODEL_REGISTRY.keys())}")
    if model_name not in _model_cache:
        path = MODEL_REGISTRY[model_name]["path"]
        if not os.path.exists(path):
            raise FileNotFoundError(
                f"Model file not found: '{path}'. "
                "Run Sprint6_Model_Benchmarking.ipynb to generate model files."
            )
        _model_cache[model_name] = joblib.load(path)
        print(f"✅ Loaded '{model_name}' from {path}")
    return _model_cache[model_name]


def error_response(message: str, status: int = 400):
    return jsonify({"success": False, "error": message}), status


